"""3 - Faça um programa, utilizando Dicionários, que peça para o usuário inserir
 quatro notas e mostre na tela as notas e a média entre elas.
"""

notas = {}
soma = 0

for i in range(1, 5):
    nota = float(input(f"Digite a {i}ª nota: "))
    notas[f"Nota {i}"] = nota
    soma += nota

media = soma / 4

print("\nNotas inseridas:")
for key, value in notas.items():
    print(f"{key}: {value}")
print(f"Média: {media}")

