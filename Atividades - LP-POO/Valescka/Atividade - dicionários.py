"""5 - Faça um programa, utilizando Dicionários, que:
    1° Passo: Peça para o usuário inserir o nome de três funcionários e os
mostre na tela; 2° Passo: Peça para o usuário demitir um funcionário e mostre na tela os funcionários restantes.
"""

funcionarios = {}

for i in range(1, 4):
    nome = input(f"Digite o nome do {i}º funcionário: ")
    funcionarios[i] = nome

print("\nFuncionários inseridos:")
for chave, valor in funcionarios.items():
    print(f"{chave}: {valor}")

num_demissao = int(input("\nDigite o número do funcionário que deseja demitir: "))

if num_demissao in funcionarios:
    del funcionarios[num_demissao]
else:
    print("Funcionário não encontrado.")

print("\nFuncionários restantes:")
for chave, valor in funcionarios.items():
    print(f"{chave}: {valor}")
